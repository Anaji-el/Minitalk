#include <stdio.h>

int	ft_atoi(const char *str)
{
	int	i;
	int	r;

	i = 0;
	r = 0;

	while (str[i] >= '0' && str[i] <= '9')
	{
		r = r * 10 + (str[i] - '0');
		i++;
	}
	return (r);
}